package project1;

public class Relational {

	public static void main(String[] args) {
		int a =4,b=2;
		
		//IS EQUAL TO
		System.out.println("a==b: "+(a==b));
		
		//IS NOT EQUAL TO
		System.out.println("a!=b: "+(a!=b));
		
		//IS GREATER THAN
		System.out.println("a>b: "+(a>b));
		
		//IS LESSER THAN
		System.out.println("a<b: "+(a<b));
		
		//IS GREATER THAN EQUAL TO
		System.out.println("a>=b: "+(a>=b));
		
		//IS LESSER THAN EQUAL TO
		System.out.println("a<=b: "+(a<=b));
	}

}
