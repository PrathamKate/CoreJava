package ProjectJava;

public class Swapping {

	public static void main(String[] args) {
		int a=8,b=6;
		System.out.println("-----Before Swapping-----");
		System.out.println("The value of A: "+a);
		System.out.println("The value of B: "+b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("\n------After Swapping-------");
		System.out.println("The value of A: "+a);
		System.out.println("The value of B: "+b);
	}

}
